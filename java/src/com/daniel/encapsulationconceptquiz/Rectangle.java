package com.daniel.encapsulationconceptquiz;

public class Rectangle {
    private int length;
    private int width;

    public Rectangle(int length, int width) {
        this.length = length;
        this.width = width;
    }

    public void printArea() {
        System.out.println("Area of rectangle: " + getLength()*getWidth() + "cm2");
        System.out.println("===========================");
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
